export default {

	methods: {

		purifyBadWords(str) {
			const regex = /[^ ,.?!:"'\-)]*(ху[е|й|и]|п[и|е|ё]зд|[е|ё]б[а|у|л]|[е|ё]б[а|о][л|ш|н]|бля[т|д]|г[о|а]ндон|з[а|о]луп|муд[а|и][к|л]|пид[о|а]р|с(у|уч)к|др[о|а]ч|[е|ё]лда|д[а|о]лб[а|о][е|ё]б|дерьмо)[^ ,.?!:"'\-)]*/gi;
			return str.replace(regex, (match) => '*'.repeat(match.length)) || str;
		}
	}

}